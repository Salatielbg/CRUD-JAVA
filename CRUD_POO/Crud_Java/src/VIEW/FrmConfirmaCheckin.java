    private boolean AlterarCheckin() {
        String id_reserva, data_checkin, status;

        data_checkin = txtDataEntrada.getText();
        id_reserva = txtIdReserva.getText();

        String formataDatacheckin = convertDateFormat(data_checkin);

        if (formataDatacheckin == null) {
            JOptionPane.showMessageDialog(null, "Formato de data inválido. Por favor, use o formato dd/MM/yyyy.");
            return false;
        }

        ReservaDTO objCadReservaDTO = new ReservaDTO();
        status = "Ativo";
        objCadReservaDTO.setDataCheckin(formataDatacheckin);
        objCadReservaDTO.setId(id_reserva);
        objCadReservaDTO.setStatus(status);

        ReservaDAO objCadReservaDAO = new ReservaDAO();

        try {
            objCadReservaDAO.AlterarDataCheckin(objCadReservaDTO);
            JOptionPane.showMessageDialog(null, "Data de check-in atualizada");
            return true; // Retorna true se a operação for bem-sucedida
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao atualizar a data de check-in: " + e.getMessage());
            return false; // Retorna false se houver um erro ao alterar a data de check-in
        }

    }

    private String convertDateFormat(String dateStr) {
        try {
            SimpleDateFormat inputFormat = new SimpleDateFormat("dd/MM/yyyy");
            SimpleDateFormat outputFormat = new SimpleDateFormat("yyyy-MM-dd");
            Date date = inputFormat.parse(dateStr);
            return outputFormat.format(date);
        } catch (ParseException e) {
            return null;
        }
    }
